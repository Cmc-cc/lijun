            }
        }
    $(function(){
       $(".notice_close").on('click',function(){
            $("#active_").hide();
        })
        
       $('.joinActive').click(function(){
            if(vid!=0){
                $.ajax({
                    url:"<?php echo e(route('member.apply_activity')); ?>",
                    type:'post',
                    data:{activity_id:vid},
                    success:function(data){
						if(data.status.errorCode == 0){
